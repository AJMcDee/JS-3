// Indices tell us where a piece of information is stored

// Example 1: A string

var name = "Anne";
console.log(name[0]); //log "A"
name[3] = "a";

// Example 2: An array

let arr1 = [1, 2, 3, 4, 5];

arr1[2] = 35;
console.log(arr1); //[1,2,35,4,5]

